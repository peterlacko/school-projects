        private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
		GeneticAlgorithm ga = new GeneticAlgorithm();
		ga.setPopulationSize(sldPopulationSize.getValue());
		ga.setReproductionCycles(sldReproductionCycles.getValue());
		ga.setParentSelection(selParentSelection.getSelectedIndex());
		ga.setRecombinationType(selRecombinationType.getSelectedIndex());
		ga.setReinsertion(sldReinsertion.getValue());
		ga.initPopulation(knapsack, maxKnapsackVolume);
		ga.performCycle();
		GASimulationUI simFrame = new GASimulationUI();
		simFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
		simFrame.setVisible(true);
		simFrame.myInit(ga);
        }//GEN-LAST:event_btnConfirmActionPerformed

