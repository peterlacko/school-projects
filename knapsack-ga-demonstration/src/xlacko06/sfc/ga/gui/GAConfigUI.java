	private void generateKnapsack() {
		Item x;
		knapsack.clear();
		for (int i = 0; i < 8; i++) {
			x = new Item();
			knapsack.add(x);
		}
		maxKnapsackVolume = 0;
		for (Item i:knapsack) {
			maxKnapsackVolume += 128 + (int)i.getVolume();
		}
		maxKnapsackVolume /= 2;
	}

	private void drawKnapsack() {
		String vol = "";
		String val = "";
		for (Item i: knapsack) {
			vol+= Integer.toString((int)(i.getVolume())+128) + " \n";
			val+= Integer.toString((int)(i.getValue())+128) + " \n";
		}
		vol = vol.substring(0, vol.length()-1);
		val = val.substring(0, val.length()-1);
		jTextPane2.setText(vol);
		jTextPane3.setText(val);
	}
